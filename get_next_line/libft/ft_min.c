#include "libft.h"

int		ft_min(int *tab, size_t len)
{
	size_t	i;
	int		min;

	min = tab[0];
	i = 0;
	while (i < len - 1)
	{
		if (tab[i + 1] < min)
		{
			min = tab[i + 1];
		}
		i++;
	}
	return (min);
}
