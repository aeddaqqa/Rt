#include "libft.h"

int		ft_point(char *str)
{
	int		i;
	int		count;

	i = 0;
	count = 0;
	while (str[i])
	{
		if (str[i++] == '.')
			count++;
	}
	if (count == 1 || count == 0)
		return (1);
	return (0);
}
