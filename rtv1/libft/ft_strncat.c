#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t nb)
{
	size_t		i;
	size_t		j;

	i = 0;
	while (s1[i])
		i++;
	j = 0;
	while (s2[j] && j < nb)
	{
		s1[i++] = s2[j++];
	}
	s1[i] = '\0';
	return (s1);
}
