#include "../../includes/rt.h"

t_vect3	vect_prod(t_vect3 u, t_vect3 v)
{
	t_vect3	w;

	w.x = u.x * v.x;
	w.y = u.y * v.y;
	w.z = u.z * v.z;
	return (w);
}
