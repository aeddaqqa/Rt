#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	unsigned int	i;
	char			*res;

	if (!(s && f))
		return (0);
	res = (char *)malloc((ft_strlen(s) + 1) * sizeof(char));
	if (res == 0)
		return (0);
	i = 0;
	while (s[i])
	{
		res[i] = f(s[i]);
		i++;
	}
	res[i] = '\0';
	return (res);
}
