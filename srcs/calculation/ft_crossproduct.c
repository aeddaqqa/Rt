#include "../../includes/rt.h"

t_vect3		cross(t_vect3 a, t_vect3 b)
{
	t_vect3	c;

	c.x = (a.y * b.z) - (a.z * b.y);
	c.y = (a.z * b.x) - (a.x * b.z);
	c.z = (a.x * b.y) - (a.y * b.x);
	return (c);
}
