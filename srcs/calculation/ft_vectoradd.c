#include "../../includes/rt.h"

t_vect3		vect_add(t_vect3 a, t_vect3 b)
{
	t_vect3	c;

	c.x = a.x + b.x;
	c.y = a.y + b.y;
	c.z = a.z + b.z;
	return (c);
}
